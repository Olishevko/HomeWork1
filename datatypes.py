#Числа
int_num = 10
print(int_num, type(int_num))

float_num = 5.5
print(float_num, type(float_num))

complex_num = 1+2j
print(complex_num, type(complex_num))

#Строки
string = 'Some string'
print(string, type(string))

#Списки
my_list = [1, 'world', 13+3j]
print(my_list, type(my_list))

#Кортежи
my_tuple = (1, '123',4,1,6,3, None, 7, 1, 9, 3)
print(my_tuple, type(my_tuple))


#Множиства
my_set = {1, '123',4,1,6,3, None, 7, 1, 9, 3}
print(my_set, type(my_set))

#Словари
my_dict = {
	'some_key': 'some_value',
	'number': 10
	}
print(my_dict, type(my_dict))
